package Day13.Ex04_수강과정등록;

public class Person {
	// 생성자
	// getter, setter
	// toString

	private String name;

	public Person(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Person [name=" + name + "]";
	}
	
}
