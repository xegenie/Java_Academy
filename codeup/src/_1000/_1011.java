package _1000;

import java.util.Scanner;

/*
 * 문자형(char)으로 변수를 하나 선언하고, 변수에 문자를 저장한 후
변수에 저장되어 있는 문자를 그대로 출력해보자.
 */

public class _1011 {
	
	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		String a = sc.next();
		
		System.out.print(a);
		
		sc.close();
	}

}
